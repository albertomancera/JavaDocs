package prog.unidad04.atributos.ejercicio05;

/**
 * Intervalo de tiempo con horas minutos y segundos
 */
public class IntervaloTiempo {

  /**
   * Numero de horas. No debería ser menor a cero
   */
  public int horas;
  /**
   * Numero de minutos del intervalo. Debería estar entre 0 y 59
   */
  public int minutos;
  /**
   * Numero de segundos del intervalo. Debería estar enter 0 y 59
   */
  public int segundos;
}
