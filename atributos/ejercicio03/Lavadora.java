package prog.unidad04.atributos.ejercicio03;

/**
 * Clase que representa a una lavadora
 */
public class Lavadora {

  /**
   * Precio base de la lavadora (debe ser mayor que cero)
   */
  public double precioBase;
  
  /**
   * Color de la lavadora (valores válidos: blanco, negro, rojo, azul, gris)
   */
  public String color;
  
  /**
   * Calificación energética (A, B, C, D, E, F)
   */
  public char calificacionEnergetica;
  
  /**
   * Peso de la lavadora (en kilogramos. Debe ser mayor que cero)
   */
  public double peso;
  
  /**
   * Carga máxima de la lavadora (en kilogramos. Debe ser mayor que cero)
   */
  public double cargaMaxima;
}
